/*function*/
function func1(a,b){
var c= a+b;
return c;
}
var a=5;
var b=10;
console.log(func1(a,b));

/*anonymous function*/
var c = function(a,b){
    return a+b;
}
var a=20;
var b=10;
console.log(c(a,b));

/*arrow function*/
var c=(a,b)=>{
    return a+b;
}
var a=50;
var b=50;
console.log(c(a,b));






  
  
  
  